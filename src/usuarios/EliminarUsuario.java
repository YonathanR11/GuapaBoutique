    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        try {
            Statement comando = cn.createStatement();
            ResultSet registro = comando.executeQuery("SELECT * FROM usuarios WHERE idusuario='" + lbid.getText() + "'");
            if (registro.next() == true) {
                comando.executeUpdate("DELETE FROM usuarios WHERE idusuario='" + lbid.getText() + "'");
                JOptionPane.showMessageDialog(null, "Datos Borrados Correctamente!");
                usuarios.pruebausuarios ver = new usuarios.pruebausuarios();
                ver.mostrardato();
                this.dispose();
            } else {
//                JOptionPane.showMessageDialog(null, "Ocurrio un problema!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(EliminarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_borrarActionPerformed

